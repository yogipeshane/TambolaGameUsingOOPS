class Player:
    def setPlayerName(self, playerName):
        self.playerName = playerName

    def getPlayerName(self):
        return self.playerName

    def setPlayerValues(self, playerValues):
        self.playerValues = playerValues

    def getPlayerValues(self):
        return self.playerValues

    def setPlayerCurrentScore(self, playerCurrentValues):
        self.playerCurrentValues = playerCurrentValues

    def getPlayerCurrentScore(self):
        return self.playerCurrentValues
